function darkWhiteMode() {
    const button = document.getElementById("button");
    document.body.classList.toggle("White_mode")
    if (button.innerText === 'white-mode'){
        button.innerText = 'dark-mode'
    }else {
        button.innerText = 'white-mode'
    }
}
